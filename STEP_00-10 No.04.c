/* No.04の関数１ */
void func4(int *p) {
	*p = 11;

	while (*p <= 20) {		/* pの値が20以下の場合 ループする */
		printf("%d", *p);	/* pの値を出力 */

		if (*p == 20) {
			break;			/* pの値が 20 の場合 ループ終了 */
		}

		(*p)++;				/* pの値をインクリメント */
	}
	return;
}


